      <td><h2>Why should I donate platelets?</h2><br>
       <h3></h3> A continuous supply of fresh platelets is essential to meet the needs of patients in area hospitals.<br> Unlike whole blood, the shelf life of a unit of platelets is only five days, <br>so platelet donors are needed on a consistent basis in order to keep a full supply.</h3></td>
    </tr>
    <tr>
      <td><h2>Who uses platelets?</h2><BR>
        <h3></h3>A wide variety of patients need platelets. Children and adults with cancer use platelets,<br> as well as people undergoing organ transplants, bone marrow transplants, and heart surgery.<br> For example, patients with leukemia undergo chemotherapy to destroy the cancerous white cells, <br>but this treatment also destroys their healthy cells, leaving the patient vulnerable to infection.</h3></td>
